package loops;

public class For_Execucao {

	public static void main(String[] args) {
		// Teste de execução com incrementacao
		for (int i = 0; i < 5; i++) {
			System.out.printf("Valor de i: %d \n", i);
		}
		
		System.out.println("\n");
		
		// Teste de execução com incrementacao
		for (int j = 5; j >= 0; j--) {
			System.out.printf("Valor de j: %d \n", j);
		}
	}

}
